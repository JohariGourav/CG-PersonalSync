//Class Maths with basic Calculator Methods ( Add, Subtract, Divide, Multiply)
package in.co.capgemini.calculator;

public class Maths {

	// method to add 2 numbers
	public int add(int number1, int number2) {

		return number1 + number2;
	}

	// method to subtract 2 numbers
	public int subtract(int number1, int number2) {

		return number1 - number2;
	}

	// method to multiply 2 numbers
	public int multiply(int number1, int number2) {

		return number1 * number2;
	}

	// method to divide 2 numbers
	public int division(int number1, int number2) {

		return number1 / number2;
	}

}
